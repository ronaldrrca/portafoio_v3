                        <h3>Credibilidad</h3>
                        <p>"La primera impresión es la que vale", tener una página o sitio web es una oportunidad para crear una gran buena impresión y transmitirle a los clientes potenciales la tranquilidad de 
                            están viendo un negocio real. Una manera de sobre salir sobre la competencia puede ser tener una página o sitio que luzca muy bien y contenga información de calidad para sus clientes.
                        </p>
                        <h3>Marca</h3>
                        <p>Dar a conocer la "marca" es una de las cosas importantes que se puede hacer por un emprendimiento, y es muy difícil de lograr sin una página o sitio web donde se pueda encontrar información
                            información de calidad y creible por parte de los clientes potenciales, permitiéndoles conocer quién eres, qué haces y qué ofreces.</p>
                        <h3>Clientes potenciales</h3>
                        <p>No es sufuciente solo con las redes sociales, cuando un cliente potencial encuentra a un negocio en cualquier red social y se interesa por un producto o servicio, lo más seguro y razonable es que
                            quiera buscar más información sobre la empresa, y realizará una búsqueda en la web. Sí el negocio no cuenta con esta presencia, las propbabilidades de concretar la venta seguramente serán muy bajas
                            en relación al hecho de tener una página o sitio web. 
                        </p>
                        <h3>Tráfico</h3>
                        <p>Tener una página o sitio web con optimización para motores de búsqueda es una gra oportunidad de aparecer en los resultados de búsqueda de Google, lo que significa que cuando una persona esté buscando
                            productos o servicios que le puedas ofrecer, vas a aparecer en esos resultados e incrementarás drásticamente tu basede clientes.
                        </p>
                        <h3>Ahorro de tiempo + servicio al cliente</h3>
                        <p>Puedes ahorrar tiempo colocando la información o las preguntas más frecuentes que realizan tus clientes cuando llaman por teléfono, preguntas coom la dirección, horarios de atención, si hacen domicilios, etc.
                            Podrás enfocar ese tiempo en mejorar la productividad y le entregarás a tus clientes potenciales la información al instante. Por otro lado, el no poder contestar una llamada por cualquier circunstancia, 
                            podría resultar en que el cliente busque otra empresa que le ofrezca lo que está buscando.
                        </p>
                        <h3>Actualizaciones y anuncios</h3>
                        <p>Mantener la información del negocio actualizada le brinda a tus clientes la oportunidad de acceder a ella cualquier día y a cualquier hora, además, de mantener a tus clientes actializados con nuevos productos, 
                            servicios u ofertas. </p>
                        <h3>Marketing digital</h3>
                        <p>Actualmente las redes sociales son un medio importante para llegar a tus clientes, pero serán muchísimo más efectivas si tienes una página o sitio web a donde redireccionar a esos clientes potenciales para ofrecerle
                            más información y obtener algún medio de contacto para enviarle futuras ofertas, por ejemplo. Además, si quieres hacer alguna publicidad mediante los ads de Google, Faceboo, Twitter, etc, necesitaras una página o sitio 
                            web para que tus clientes puedan llegar a través de este medio.</p>
